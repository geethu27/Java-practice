public class Datatypes {
    public static void main(String[] args) {
   //   BOOLEAN DATATYPE
        Boolean isJavaFun = true;
        Boolean isFishTasty = false;
        System.out.println("Is java fun: " + isJavaFun);
        System.out.println("Is Fish tasty: " + isFishTasty);  



        // BYTE DATATYPE  (-128  to  127)
        byte age = 20;
        byte temperature = - 12;
        System.out.println("Age: " + age);
        System.out.println("Temperature:" + temperature);  


        //  SHORT DATATYPE  (-32,768   to   32,767)
        short students = 1000;
        short temp = -200;
        System.out.println("Students : " + students);
        System.out.println("Temperature : " + temp);


        // INT DATATYPE  (-2,147,483,648   to   2,147,483,647)
        int population = 2000000;
        int distance = 12443343;
        System.out.println("Population : " + population);
        System.out.println("Distance : " + distance);


        
        // LONG DATATYPE
        long world_population = 200000435L;
        long light_years = 324343L;
        System.out.println("World Population : " + world_population);
        System.out.println("Light Years :" + light_years);


        // FLOAT DATATYPE
        float pi =5.32f;
        float gravity = 32.1f;
        System.out.println("Pi : " + pi);
        System.out.println("Gravity : " + gravity);



        // DOUBLE DATATYPE
        double apple = 3.231243545;
        double avogadro = 65.23456;
        System.out.println("Apple: " + apple);
        System.out.println("Avogadro: " + avogadro);


        // CHAR DATATYPE
        char grade = 'A';
        char symbol = '$';
        System.out.println("Grade: " + grade);
        System.out.println("Symbol: " + symbol);



        // STRINGS
        String message = "Hello";
        String name = "Geethika";
        System.out.println("Message: " + message);
        System.out.println("Name: " + name);

    }   
} 


